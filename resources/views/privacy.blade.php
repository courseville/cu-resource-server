  <h1>Privacy Policy for CU-ENG Central Data Platform API</h1>
  <p><strong>Effective Date:</strong> May 28, 2025</p>

  <p>Chulalongkorn University Faculty of Engineering ("CU-ENG," "we," "our," or "us") respects the privacy of users interacting with the CU-ENG Central Data Platform API ("API"). This Privacy Policy explains how we collect, use, and protect information when you access or use the API.</p>

  <hr>

  <section>
    <h2>1. Information We Collect</h2>
    <p>The API is designed to provide official personnel and organizational information only. It does <em>not</em> collect or process personal data of end-users accessing the API. Specifically, the API:</p>
    <ul>
      <li>Provides <em>public institutional data</em> such as personnel names, academic titles, organizational affiliations, and structural hierarchies.</li>
      <li>Does <em>not</em> track user identities, IP addresses, device information, or geolocation.</li>
      <li>Does <em>not</em> store cookies or other user-identifying technologies.</li>
    </ul>
  </section>

  <section>
    <h2>2. How We Use Information</h2>
    <p>Any data provided through the API is:</p>
    <ul>
      <li>Used solely for informational and directory purposes.</li>
      <li>Maintained to ensure accuracy, consistency, and up-to-date organizational representation.</li>
      <li>Not shared, sold, or used for commercial advertising or profiling.</li>
    </ul>
  </section>

  <section>
    <h2>3. Data Security</h2>
    <p>We implement appropriate technical and organizational measures to safeguard the integrity and availability of the data provided via the API, including:</p>
    <ul>
      <li>Access controls and API key management (if applicable).</li>
      <li>Regular updates and maintenance to ensure system security.</li>
      <li>Monitoring for unauthorized access or misuse.</li>
    </ul>
    <p>Please note that since the API delivers publicly available institutional data, there is no sensitive personal information processed that would require advanced encryption or data minimization measures.</p>
  </section>

  <section>
    <h2>4. Third-Party Access</h2>
    <p>The API may be integrated into third-party applications or services; however, CU-ENG is <em>not responsible</em> for how third parties use, store, or process the data retrieved from the API. We recommend that developers and integrators comply with their own privacy obligations and clearly inform their users.</p>
  </section>

  <section>
    <h2>5. User Rights</h2>
    <p>As the API only provides public institutional data, it does not process individual personal data subject to rights under data protection laws (e.g., GDPR, PDPA). However, CU-ENG is committed to ensuring the accuracy of the data presented.</p>
    <p>If you believe any published information is inaccurate or requires correction, please contact the CU-ENG administrative office.</p>
  </section>

  <section>
    <h2>6. Changes to This Privacy Policy</h2>
    <p>We may update this Privacy Policy from time to time to reflect changes in legal requirements, technology, or organizational practices. We will post the updated policy on our official website with the effective date.</p>
  </section>

